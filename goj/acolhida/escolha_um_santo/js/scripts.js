const santoDivs = document.querySelectorAll('.santo');

santoDivs.forEach((santoDiv) => {
    santoDiv.addEventListener('click', () => {
        const santo = santoDiv.getAttribute('data-santo');
        const mensagensSanto = mensagens[santo];
        const mensagem = mensagensSanto[Math.floor(Math.random() * mensagensSanto.length)];
        exibirMensagem(mensagem);
    });
});

function exibirMensagem(mensagem) {
    const mensagemTexto = document.getElementById('messagem-texto');
    mensagemTexto.textContent = mensagem;
}